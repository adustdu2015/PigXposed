package com.example.pigxposed.bean;

public class Point {
    float press;
    float f2424x;
    float f2425y;

    public Point(float f, float f2, float f3) {
        this.f2424x = f;
        this.f2425y = f2;
        this.press = f3;
    }

    public float getPress() {
        return this.press;
    }

    public void setPress(float f) {
        this.press = f;
    }

    public float getX() {
        return this.f2424x;
    }

    public void setX(float f) {
        this.f2424x = f;
    }

    public float getY() {
        return this.f2425y;
    }

    public void setY(float f) {
        this.f2425y = f;
    }
}